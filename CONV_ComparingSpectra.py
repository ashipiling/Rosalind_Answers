def comparingSpectra(s1, s2):
    dict = {}
    for i in s1:
        for j in s2:
            d = i - j
            if d in dict:
                dict[d] += 1
            else:
                dict[d] = 1
    out = max((v, k) for k, v in dict.items())
    return out

